public class Meal {
    private String name;
    private String desc;
    private String img;

    public String getName(){
        return name;
    }

    public String getDesc(){
        return desc;
    }

    public String getImg(){
        return img;
    }

    public void setName(String s){
        name = s;
    }

    public void setDesc(String s){
        desc = s;
    }

    public void setImg(String s){
        img = s;
    }
}

